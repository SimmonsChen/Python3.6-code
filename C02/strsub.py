
str1 = 'Hello World!'
str2 = "I'm fine,and you?"


##print ("str1原文: ", str1) 
##print ("str2原文: ", str2)

print ("1.str1[1]: ", str1[1]) #正向编号从0开始
print ("2.str1[2:7]: ", str1[2:7]) # 表示从第3个数起，到第七个数
print ("3.str1[-3:-1]: ", str1[-3:-1]) # 倒数第三个数开始，到倒数第一， 运算从第一个参数到第二个参数的顺序，不可逆序运算
print ("4.str1[5:5]: ", str1[5:5])

print ("1.str2[0]: ", str2[0])
print ("2.str2[:5]: ", str2[:5])# 第一个数为空，从头开始截取
print ("3.str2[6:]: ", str2[6:])#截取第七个参数以及之后
print ("4.str2[-2:]: ", str2[-2:])#从倒数第二个参数截取，到最后
print ("5.str2[:-2]: ", str2[:-2])# 从头开始截取，一直到倒数第二位

